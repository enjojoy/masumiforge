/**
 * Main forge function — scaffolds a new Masumi agent from a description.
 */
export async function forge(params: ForgeParams, _config: any): Promise<any> {
  const description = params.description;
  const framework = params.framework || "basic";
  const outputDir = path.resolve(params.outputDir || "./agent");

  // Create output directory
  fs.mkdirSync(outputDir, { recursive: true });

  const files: string[] = [];

  // Generate agent.py
  const agentPyPath = path.join(outputDir, "agent.py");
  fs.writeFileSync(agentPyPath, generateAgentPy(description, framework));
  files.push("agent.py");

  // Generate .env.example
  const envPath = path.join(outputDir, ".env.example");
  fs.writeFileSync(envPath, generateEnvExample());
  files.push(".env.example");

  // Generate requirements.txt
  const reqPath = path.join(outputDir, "requirements.txt");
  fs.writeFileSync(reqPath, generateRequirements(framework));
  files.push("requirements.txt");

  // Generate README.md
  const readmePath = path.join(outputDir, "README.md");
  fs.writeFileSync(readmePath, generateAgentReadme(description, framework));
  files.push("README.md");

  const summary = `✅ Forged Masumi agent in \`${outputDir}\`

**Files created:**
${files.map((f) => `  • ${f}`).join("\n")}

**Description:** ${description}
**Framework:** ${framework}

---

**Next steps:**

1. \`cd ${outputDir}\`
2. \`pip install -r requirements.txt\`
3. \`cp .env.example .env\` — fill in your Payment Service details
4. Implement your logic in \`agent.py\` → \`process_job()\`
5. \`masumi run agent.py\` — starts your agent on port 8000
6. Register at **http://localhost:3001/admin** to get your \`AGENT_IDENTIFIER\`
7. Add \`AGENT_IDENTIFIER=<your_id>\` to \`.env\` and restart
8. 🎉 You're live on [Sokosumi](https://preprod.sokosumi.com/agents)!

**Test locally:**
\`\`\`bash
curl -X POST http://localhost:8000/start_job \\
  -H "Content-Type: application/json" \\
  -d '{"identifier_from_purchaser": "test-001", "input_data": [{"key": "input", "value": "test"}]}'
\`\`\``;

  return {
    content: [{ type: "text", text: summary }],
  };
}
