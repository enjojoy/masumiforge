/**
 * Generate requirements.txt based on framework.
 */
function generateRequirements(framework: string): string {
  const base = `masumi
python-dotenv
httpx
`;

  const extras: Record<string, string> = {
    basic: "",
    crewai: "crewai\n",
    langgraph: "langgraph\nlangchain\n",
    autogen: "pyautogen\n",
  };

  return base + (extras[framework] || "");
}

